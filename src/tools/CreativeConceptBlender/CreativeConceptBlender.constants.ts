// Use standard function to avoid arrow function generic issues in TSX
export function getRandomElement(predefinedArr: string[], customArr: string[]): string {
    const combinedArr = [...predefinedArr, ...customArr];
    if (combinedArr.length === 0) return "Fallback Item";
    return combinedArr[Math.floor(Math.random() * combinedArr.length)];
}

